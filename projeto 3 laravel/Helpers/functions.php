/**
 * Move o arquivo para a pasta destino e retorna o caminho.
 *
 * @param \Illuminate\Http\Request::file  $file
 * @param string $dir - diretorio em que o arquivo será salvo.
 * @return string - caminho para o arquivo.
 */
function prepareFile($file, $dir)
{
    try {
        $name = Str::slug(explode('.', $file->getClientOriginalName())[0]);
        $fileName = $name . "_". rand(1111, 9999) . "." . $file->guessClientExtension();
        $file->move($dir, $fileName);

        return $dir . "/" . $fileName;
    } catch (\Exception $e) {
        return $e->getMessage();
    }
}

/**
 * Create File Name Without Accents
 * @param String $string
 * @return String
 */
function create_file_name_from_existing_name($string)
{
    // Trata acentos
    $chars_Accent = get_accents_chars();
    $chars_no_Accent = get_no_accents_chars();
    return str_replace($chars_Accent, $chars_no_Accent, $string);
}

